"use client";

export default function ProvenancePanel({
  briefType,
  groundingSources,
  evidenceQuality,
  confidenceNote,
}: {
  briefType: "research" | "diligence";
  groundingSources: string[];
  evidenceQuality?: string;
  confidenceNote?: string;
}) {
  const hasEngineData = groundingSources.length > 0 &&
    !groundingSources.some((s) => s.toLowerCase().includes("no ") && s.toLowerCase().includes("available"));

  return (
    <div className="rounded border border-[var(--border)] bg-[var(--bg-primary)] px-4 py-3 space-y-2">
      <h4 className="text-[10px] font-semibold text-[var(--text-muted)] uppercase tracking-wider">
        Provenance &amp; Grounding
      </h4>

      {/* Source type */}
      <div className="flex items-center gap-2">
        <span className="text-[10px] text-[var(--text-muted)]">Generated by:</span>
        <span className="text-[10px] px-1.5 py-0.5 rounded border border-[var(--accent-purple)]/30 text-[var(--accent-purple)]">
          AI (DeepSeek)
        </span>
      </div>

      {/* Grounding status */}
      <div className="flex items-center gap-2">
        <span className="text-[10px] text-[var(--text-muted)]">Engine data used:</span>
        <span
          className={`text-[10px] px-1.5 py-0.5 rounded border ${
            hasEngineData
              ? "border-[var(--accent-green)]/30 text-[var(--accent-green)]"
              : "border-[var(--accent-amber)]/30 text-[var(--accent-amber)]"
          }`}
        >
          {hasEngineData ? "Yes — grounded in structured outputs" : "None available — ungrounded"}
        </span>
      </div>

      {/* Evidence quality (research only) */}
      {evidenceQuality && (
        <div className="flex items-center gap-2">
          <span className="text-[10px] text-[var(--text-muted)]">Evidence quality:</span>
          <span className="text-[10px] text-[var(--text-secondary)]">{evidenceQuality}</span>
        </div>
      )}

      {/* Confidence note (diligence only) */}
      {confidenceNote && (
        <div>
          <span className="text-[10px] text-[var(--text-muted)]">Confidence:</span>
          <p className="text-[10px] text-[var(--text-secondary)] mt-0.5 leading-snug">{confidenceNote}</p>
        </div>
      )}

      {/* Sources */}
      {groundingSources.length > 0 && (
        <div>
          <span className="text-[10px] text-[var(--text-muted)]">Sources:</span>
          <ul className="mt-0.5 space-y-0.5">
            {groundingSources.map((s, i) => (
              <li key={i} className="text-[10px] text-[var(--text-secondary)]">
                {s}
              </li>
            ))}
          </ul>
        </div>
      )}

      {/* Trust notice */}
      <p className="text-[9px] text-[var(--text-muted)] italic border-t border-[var(--border)] pt-2 mt-2">
        This {briefType} brief was AI-generated and should be reviewed before use in decisions.
        Facts from structured engine outputs are canonical; other analysis is LLM synthesis.
      </p>
    </div>
  );
}
